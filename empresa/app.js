
$(document).ready(function(){
    $('#search').keyup(function(event){
	    event.preventDefault();
	    let data= $('#form').serializeArray();
	    $.post({
		    url:'action.php',
	        data:data,
	        success:function(response){
		         $('#response1').html(response);
            }
	    
	    })
    })
});

